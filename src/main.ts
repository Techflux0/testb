import { NestFactory } from '@nestjs/core';
import { ValidationPipe } from '@nestjs/common';
import { AppModule } from './app.module';
import { DocumentBuilder, SwaggerModule } from '@nestjs/swagger';

async function bootstrap() {
  const app = await NestFactory.create(AppModule);

  // Enable CORS for your React Native app
  app.enableCors({
    origin: ['http://localhost:3000', 'http://localhost:8081'], // React & React Native
    credentials: true,
  });

  // Global validation pipe
  app.useGlobalPipes(new ValidationPipe({
    whitelist: true,
    forbidNonWhitelisted: true,
    transform: true,
  }));

  // Swagger documentation
  const config = new DocumentBuilder()
    .setTitle('Trivia Pro API')
    .setDescription('Trivia Pro Authentication and User Management API')
    .setVersion('1.0')
    .addBearerAuth()
    .build();
  const document = SwaggerModule.createDocument(app, config);
  SwaggerModule.setup('api', app, document);

  await app.listen(3000);
  console.log('ðŸš€ Trivia Pro API running on http://localhost:3000');
  console.log('ðŸ“š API Documentation: http://localhost:3000/api');
}
bootstrap();